package templates

import (
	"github.com/dave/jennifer/jen"
)

// SkeletonKeychain generates cmd/<name>/keychain.go for scaffolded
// projects, blank-importing pkg/credentials/keychain so the tool
// activates OS keychain support by default. Deleting this single
// file produces a regulated-build variant with no IPC-to-keychain
// code linked — verifiable via SBOM against the linked artefact.
//
// Skipped when the "keychain" feature is opted out during
// `gtb generate` (--features=...without keychain, or the interactive
// multi-select).
func SkeletonKeychain() *jen.File {
	f := jen.NewFile("main")
	f.HeaderComment("Code generated by gtb. DO NOT EDIT.")
	f.PackageComment("Activates OS keychain support by blank-importing")
	f.PackageComment("the go-keyring-backed Backend. Delete this file to")
	f.PackageComment("ship a regulated build whose linked binary carries")
	f.PackageComment("no session-bus / keychain IPC code. See")
	f.PackageComment("docs/how-to/configure-credentials.md in the GTB repo.")

	f.Anon("github.com/phpboyscout/go-tool-base/pkg/credentials/keychain")

	return f
}
